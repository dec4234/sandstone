//! Compile-time generator for the `mc-data` crate (blocks & items).
//!
//! This script fetches `blocks.json` and `items.json` from the PrismarineJS
//! [minecraft-data](https://github.com/PrismarineJS/minecraft-data) repository for the
//! requested Minecraft version, caches them under `minecraft-data-cache/<version>/` (a
//! committable, offline fallback), and generates `content_generated.rs` into `OUT_DIR`.
//!
//! The version is chosen from the `SANDSTONE_MC_VERSION` environment variable (a
//! `ProtocolVerison` fancy name such as `1.21.11`) and resolved to a data folder through
//! the repo's `dataPaths.json`. When unset it defaults to [`DEFAULT_MC_VERSION`].
//!
//! Network access is only required the first time a version is built; afterwards (and for
//! offline / docs.rs builds) the cached JSON is used.
//!
//! Enabling the `vendored` cargo feature skips the network entirely and builds straight
//! from the committed `minecraft-data-cache/` JSON (which must already be populated for the
//! target version).

use std::collections::HashSet;
use std::error::Error;
use std::fs;
use std::path::{Path, PathBuf};

use proc_macro2::TokenStream;
use quote::{format_ident, quote};

const DEFAULT_MC_VERSION: &str = "1.21.11";

/// Base URL for raw files in the minecraft-data repo `data/` directory.
const RAW_BASE: &str = "https://raw.githubusercontent.com/PrismarineJS/minecraft-data/master/data";

fn main() {
	println!("cargo:rerun-if-env-changed=SANDSTONE_MC_VERSION");
	println!("cargo:rerun-if-changed=build.rs");

	let version = std::env::var("SANDSTONE_MC_VERSION").unwrap_or_else(|_| DEFAULT_MC_VERSION.to_string());
	// Set by cargo when the `vendored` feature is enabled: build from the cache, no network.
	let vendored = std::env::var("CARGO_FEATURE_VENDORED").is_ok();
	let out_dir = PathBuf::from(std::env::var("OUT_DIR").expect("OUT_DIR set by cargo"));
	let manifest_dir = PathBuf::from(std::env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR set by cargo"));
	let cache_root = manifest_dir.join("minecraft-data-cache");

	// Resolve the data folders for this version via dataPaths.json.
	let data_paths_raw = obtain(&format!("{RAW_BASE}/dataPaths.json"), &cache_root.join("dataPaths.json"), vendored, "dataPaths.json");
	let data_paths: serde_json::Value = serde_json::from_str(&data_paths_raw).expect("dataPaths.json is valid JSON");

	let entry = data_paths
		.get("pc")
		.and_then(|pc| pc.get(&version))
		.unwrap_or_else(|| panic!("version `{version}` not found under `pc` in dataPaths.json"));
	let blocks_folder = entry.get("blocks").and_then(|v| v.as_str()).unwrap_or_else(|| panic!("no `blocks` data path for version `{version}`"));
	let items_folder = entry.get("items").and_then(|v| v.as_str()).unwrap_or_else(|| panic!("no `items` data path for version `{version}`"));

	let version_cache = cache_root.join(&version);

	let blocks_json = obtain(&format!("{RAW_BASE}/{blocks_folder}/blocks.json"), &version_cache.join("blocks.json"), vendored, "blocks.json");
	let items_json = obtain(&format!("{RAW_BASE}/{items_folder}/items.json"), &version_cache.join("items.json"), vendored, "items.json");

	// Embed the raw JSON so the library can parse it at runtime via include_str!.
	fs::write(out_dir.join("blocks.json"), &blocks_json).expect("write blocks.json to OUT_DIR");
	fs::write(out_dir.join("items.json"), &items_json).expect("write items.json to OUT_DIR");

	// Generate the per-entry namespaces.
	let blocks: Vec<Entry> = parse_entries(&blocks_json, "blocks.json");
	let items: Vec<Entry> = parse_entries(&items_json, "items.json");

	let blocks_ns = gen_namespace("Block", "BlockInfo", "block_by_id", &blocks, BLOCK_FIELDS);
	let items_ns = gen_namespace("Item", "ItemInfo", "item_by_id", &items, ITEM_FIELDS);

	let file: syn::File = syn::parse2(quote! { #blocks_ns #items_ns }).expect("generated tokens form a valid Rust file");
	let generated = format!("// @generated by build.rs from minecraft-data. Do not edit.\n\n{}", prettyplease::unparse(&file));

	fs::write(out_dir.join("content_generated.rs"), generated).expect("write content_generated.rs");

	println!("cargo:rustc-env=SANDSTONE_MC_VERSION_RESOLVED={version}");
}

/// A minimal view of a block/item used only for code generation.
struct Entry {
	id: i32,
	name: String,
	variant: String,
	/// The entry's non-null JSON fields. Drives which getters are emitted (a field counts as
	/// present only when it carries a real value) and supplies the literals baked into the
	/// `const fn` getters.
	json: serde_json::Map<String, serde_json::Value>,
}

/// Parses the `id` and `name` of every entry, retains its non-null fields, and assigns a unique
/// PascalCase struct name to each.
fn parse_entries(json: &str, what: &str) -> Vec<Entry> {
	let arr: Vec<serde_json::Value> = serde_json::from_str(json).unwrap_or_else(|e| panic!("{what} is not a JSON array: {e}"));

	let mut used: HashSet<String> = HashSet::new();
	arr.into_iter()
		.map(|v| {
			let obj = v.as_object().unwrap_or_else(|| panic!("{what}: entry is not a JSON object"));
			let id = obj.get("id").and_then(|x| x.as_i64()).unwrap_or_else(|| panic!("{what}: entry missing numeric `id`")) as i32;
			let name = obj.get("name").and_then(|x| x.as_str()).unwrap_or_else(|| panic!("{what}: entry missing `name`")).to_string();
			let variant = unique_variant(&name, &mut used);
			// Drop JSON `null`s (e.g. `hardness` on unbreakable blocks): a null value means the
			// getter is omitted entirely, just like an absent key.
			let json = obj.iter().filter(|(_, v)| !v.is_null()).map(|(k, v)| (k.clone(), v.clone())).collect();
			Entry { id, name, variant, json }
		})
		.collect()
}

/// How a getter's value is produced. Scalar/string/enum fields are baked into a `const fn`
/// returning a literal; heap-backed fields can't be `const` so they delegate to the runtime
/// data table.
enum Kind {
	I32,
	U32,
	U8,
	F32,
	Bool,
	Str,
	BoundingBox,
	/// Owned/heap field, returned by reference from the runtime table. `ret` is the return type
	/// and `expr` the body, operating on a `d: &'static <Info>` binding.
	Runtime { ret: &'static str, expr: &'static str },
}

/// One accessor to generate: the JSON key whose presence gates it, the getter name, and how its
/// value is produced.
struct FieldSpec {
	json_key: &'static str,
	getter: &'static str,
	kind: Kind,
}

