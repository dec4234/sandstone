/// Builds the `(is_const, return_type, body_expr)` for one getter, baking the JSON `val` into a
/// literal for `const` kinds.
fn getter_parts(kind: &Kind, val: &serde_json::Value) -> (bool, TokenStream, TokenStream) {
	match kind {
		Kind::I32 => {
			let n = val.as_i64().expect("integer field") as i32;
			(true, quote!(i32), quote!(#n))
		}
		Kind::U32 => {
			let n = val.as_u64().expect("unsigned field") as u32;
			(true, quote!(u32), quote!(#n))
		}
		Kind::U8 => {
			let n = val.as_u64().expect("unsigned field") as u8;
			(true, quote!(u8), quote!(#n))
		}
		Kind::F32 => {
			let f = val.as_f64().expect("number field") as f32;
			// `{:?}` always renders a decimal point, so the suffixed literal is a valid `f32`.
			let lit: TokenStream = format!("{f:?}_f32").parse().expect("float literal parses");
			(true, quote!(f32), lit)
		}
		Kind::Bool => {
			let b = val.as_bool().expect("bool field");
			(true, quote!(bool), quote!(#b))
		}
		Kind::Str => {
			let s = val.as_str().expect("string field");
			(true, quote!(&'static str), quote!(#s))
		}
		Kind::BoundingBox => {
			let variant = match val.as_str().expect("boundingBox string") {
				"block" => format_ident!("Block"),
				"empty" => format_ident!("Empty"),
				other => panic!("unknown boundingBox `{other}`"),
			};
			(true, quote!(crate::blocks::BoundingBox), quote!(crate::blocks::BoundingBox::#variant))
		}
		Kind::Runtime { ret, expr } => {
			let ret: TokenStream = ret.parse().expect("field return type parses");
			let expr: TokenStream = expr.parse().expect("field access expression parses");
			(false, ret, expr)
		}
	}
}

/// Converts a snake_case minecraft name into a unique PascalCase Rust type name.
fn unique_variant(name: &str, used: &mut HashSet<String>) -> String {
	// Build PascalCase from alphanumeric segments.
	let mut base = String::new();
	for part in name.split(|c: char| !c.is_ascii_alphanumeric()) {
		if part.is_empty() {
			continue;
		}
		let mut chars = part.chars();
		let first = chars.next().unwrap();
		base.push(first.to_ascii_uppercase());
		base.extend(chars.map(|c| c.to_ascii_lowercase()));
	}
	if base.is_empty() {
		base.push_str("Unnamed");
	}
	if base.chars().next().unwrap().is_ascii_digit() {
		base.insert(0, '_');
	}

	if used.insert(base.clone()) {
		return base;
	}
	// Deterministically de-duplicate collisions.
	let mut n = 2;
	loop {
		let candidate = format!("{base}{n}");
		if used.insert(candidate.clone()) {
			return candidate;
		}
		n += 1;
	}
}

/// Generates a module (`module`) containing one unit struct per entry. Each struct exposes a
/// getter for every field it actually carries in the source JSON (per `fields`). Scalar/string
/// fields become `const fn`s returning a baked-in literal; heap-backed fields delegate to the
/// runtime metadata table (`struct_name`) via the crate-level lookup `by_id`.
///
/// This is what enables `Block::Bedrock::get_id()` style access.
fn gen_namespace(module: &str, struct_name: &str, by_id: &str, entries: &[Entry], fields: &[FieldSpec]) -> TokenStream {
	let mod_ident = format_ident!("{module}");
	let struct_ty = format_ident!("{struct_name}");
	let by_id_ident = format_ident!("{by_id}");
	let kind = module.to_ascii_lowercase();
	let mod_doc = format!("Type-safe namespace for every {kind}. Use `{module}::<Name>::get_id()` etc.");

	let structs = entries.iter().map(|e| {
		let variant = format_ident!("{}", e.variant);
		let struct_doc = format!("`{}` (id {})\n\nThis struct was autogenerated. Check the mc-data internal crate for more info.", e.name, e.id);

		let mut getters = Vec::new();
		let mut needs_data = false;
		for spec in fields {
			let Some(val) = e.json.get(spec.json_key) else { continue };
			let getter = format_ident!("{}", spec.getter);
			let doc = format!("The `{}` field of this {kind}.", spec.json_key);
			let (is_const, ret, body) = getter_parts(&spec.kind, val);
			if is_const {
				getters.push(quote! {
					#[doc = #doc]
					pub const fn #getter() -> #ret { #body }
				});
			} else {
				needs_data = true;
				getters.push(quote! {
					#[doc = #doc]
					pub fn #getter() -> #ret {
						let d = Self::data();
						#body
					}
				});
			}
		}

		// `data()` only exists to back the non-const getters; skip it when there are none.
		let data_fn = if needs_data {
			let id = e.id;
			quote! {
				/// The full metadata row backing the non-const getters on this type.
				fn data() -> &'static crate::#struct_ty {
					crate::#by_id_ident(#id).expect("generated id has matching data")
				}
			}
		} else {
			quote!()
		};

		quote! {
			#[doc = #struct_doc]
			pub struct #variant;
			impl #variant {
				#data_fn
				#(#getters)*
			}
		}
	});

	quote! {
		#[doc = #mod_doc]
		#[allow(non_snake_case, non_camel_case_types)]
		pub mod #mod_ident {
			#(#structs)*
		}
	}
}

/// Obtains a data file, honoring the `vendored` feature.
///
/// When `vendored` is set the cache is the only source: the committed copy is read directly
/// and a missing one is a hard error. Otherwise the network is tried first, falling back to
/// the cache on failure (see [`fetch_with_cache`]).
fn obtain(url: &str, cache_path: &Path, vendored: bool, what: &str) -> String {
	if vendored {
		return fs::read_to_string(cache_path).unwrap_or_else(|_| {
			panic!(
				"`vendored` feature is enabled but no cached {what} at {}. Run a normal \
                 (non-vendored) build once for this version to populate \
                 `minecraft-data-cache/`, then commit it.",
				cache_path.display()
			)
		});
	}
	fetch_with_cache(url, cache_path).unwrap_or_else(|e| panic!("could not obtain {what}: {e}"))
}

/// Fetches `url`, writing the body to `cache_path` on success. If the request fails but a
/// cached copy exists, returns the cached copy. Returns an error only if both fail.
fn fetch_with_cache(url: &str, cache_path: &Path) -> Result<String, Box<dyn Error>> {
	match fetch(url) {
		Ok(body) => {
			if let Some(parent) = cache_path.parent() {
				fs::create_dir_all(parent)?;
			}
			fs::write(cache_path, &body)?;
			Ok(body)
		}
		Err(e) => {
			if cache_path.exists() {
				println!("cargo:warning=fetch of {url} failed ({e}); using cached copy at {}", cache_path.display());
				Ok(fs::read_to_string(cache_path)?)
			} else {
				Err(format!("fetch of {url} failed ({e}) and no cache at {}", cache_path.display()).into())
			}
		}
	}
}

/// Performs a blocking HTTP GET and returns the response body as a string (ureq 3.x API).
fn fetch(url: &str) -> Result<String, Box<dyn Error>> {
	let body = ureq::get(url).call()?.body_mut().read_to_string()?;
	Ok(body)
}
